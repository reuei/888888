(function () {
  var slides = document.getElementById('carouselSlides');
  var dotsContainer = document.getElementById('carouselDots');
  var current = 0;
  var timer = null;

  if (slides && HOME_DATA.carousel) {
    HOME_DATA.carousel.forEach(function (item, idx) {
      var slide = document.createElement('div');
      slide.className = 'carousel-slide' + (idx === 0 ? ' active' : '');
      slide.innerHTML =
        '<div class="carousel-bg" style="background-image:url(\'' + item.image + '\');background:' + item.bgColor + '"></div>' +
        '<div class="carousel-overlay" style="background:' + item.bgColor + ';opacity:0.85"></div>' +
        '<div class="carousel-content">' +
          '<h1>' + item.title + '</h1>' +
          '<h2>' + item.subtitle + '</h2>' +
          '<p class="desc">' + item.description + '</p>' +
          '<div class="carousel-btns">' +
            '<a href="' + item.btnLink + '" class="btn btn-primary">' + item.btnText + '</a>' +
            '<a href="contact.html" class="btn btn-outline">联系我们</a>' +
          '</div>' +
        '</div>';
      slides.appendChild(slide);

      var dot = document.createElement('div');
      dot.className = 'carousel-dot' + (idx === 0 ? ' active' : '');
      dot.addEventListener('click', function () { goTo(idx); });
      dotsContainer.appendChild(dot);
    });

    function goTo(n) {
      var slideEls = slides.querySelectorAll('.carousel-slide');
      var dotEls = dotsContainer.querySelectorAll('.carousel-dot');
      slideEls[current].classList.remove('active');
      dotEls[current].classList.remove('active');
      current = (n + slideEls.length) % slideEls.length;
      slideEls[current].classList.add('active');
      dotEls[current].classList.add('active');
      resetTimer();
    }

    function next() { goTo(current + 1); }
    function prev() { goTo(current - 1); }
    function resetTimer() {
      if (timer) clearInterval(timer);
      timer = setInterval(next, 5000);
    }

    document.getElementById('nextBtn').addEventListener('click', next);
    document.getElementById('prevBtn').addEventListener('click', prev);
    resetTimer();
  }

  var certGrid = document.getElementById('certGrid');
  if (certGrid) {
    HOME_DATA.certificates.forEach(function (c) {
      var card = document.createElement('div');
      card.className = 'cert-card fade-in';
      card.innerHTML =
        '<div class="cert-image"><img src="' + c.image + '" alt="' + c.title + '"></div>' +
        '<div class="cert-body">' +
          '<h4>' + c.subtitle + '</h4>' +
          '<h3>' + c.title + '</h3>' +
          '<p>' + c.desc + '</p>' +
        '</div>';
      certGrid.appendChild(card);
    });
  }

  var productGrid = document.getElementById('productGrid');
  if (productGrid) {
    HOME_DATA.products.forEach(function (p) {
      var featuresHtml = '<div class="product-features">';
      p.features.forEach(function (f) {
        featuresHtml += '<span class="product-feature">' + f + '</span>';
      });
      featuresHtml += '</div>';
      var card = document.createElement('div');
      card.className = 'product-card fade-in';
      card.innerHTML =
        '<div class="product-icon">' + p.icon + '</div>' +
        '<h3>' + p.title + '</h3>' +
        '<p>' + p.desc + '</p>' +
        featuresHtml +
        '<div class="product-price">' + p.price + '</div>' +
        '<a href="' + p.link + '" class="product-link">了解更多 →</a>';
      productGrid.appendChild(card);
    });
  }

  var partnersTrack = document.getElementById('partnersTrack');
  if (partnersTrack) {
    var html = '';
    for (var loop = 0; loop < 2; loop++) {
      HOME_DATA.partners.forEach(function (p) {
        html +=
          '<div class="partner-item">' +
            '<img src="' + p.logo + '" alt="' + p.name + '">' +
            '<span>' + p.name + '</span>' +
          '</div>';
      });
    }
    partnersTrack.innerHTML = html;
  }

  var locationList = document.getElementById('locationList');
  var mapDots = document.getElementById('mapDots');
  if (locationList) {
    var regions = {};
    HOME_DATA.locations.forEach(function (l) {
      var key = l.region.split(' · ')[0] || l.region;
      if (!regions[key]) regions[key] = [];
      regions[key].push(l);
    });
    var listHtml = '';
    Object.keys(regions).forEach(function (key) {
      listHtml += '<div class="location-group fade-in">';
      listHtml += '<h3>' + key.toUpperCase() + ' / ' + key + '</h3>';
      listHtml += '<div class="location-items">';
      regions[key].forEach(function (l) {
        listHtml +=
          '<div class="location-chip">' +
            '<div class="name">' + l.name + '</div>' +
            '<div class="desc">' + l.desc + '</div>' +
          '</div>';
      });
      listHtml += '</div></div>';
    });
    locationList.innerHTML = listHtml;

    if (mapDots) {
      var dotsSvg = '';
      HOME_DATA.locations.forEach(function (l) {
        dotsSvg +=
          '<circle cx="' + l.x + '" cy="' + l.y + '" r="6" fill="#ff6a00" stroke="#fff" stroke-width="2">' +
            '<animate attributeName="r" values="6;10;6" dur="2s" repeatCount="indefinite"/>' +
            '<animate attributeName="opacity" values="1;0.6;1" dur="2s" repeatCount="indefinite"/>' +
          '</circle>' +
          '<circle cx="' + l.x + '" cy="' + l.y + '" r="3" fill="#fff"/>';
      });
      mapDots.innerHTML = dotsSvg;
    }
  }

  if ('IntersectionObserver' in window) {
    var observer = new IntersectionObserver(function (entries) {
      entries.forEach(function (entry) {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
        }
      });
    }, { threshold: 0.1 });
    document.querySelectorAll('.fade-in').forEach(function (i) { observer.observe(i); });
  }
})();
