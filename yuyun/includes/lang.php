function detectLang(): string {
    $langs = array_keys($GLOBALS['availableLanguages']);
    if (isset($_GET['lang']) && in_array($_GET['lang'], $langs, true)) {
        setcookie('lang', $_GET['lang'], time() + 86400 * 365, '/');
        return $_GET['lang'];
    }
    if (isset($_COOKIE['lang']) && in_array($_COOKIE['lang'], $langs, true)) {
        return $_COOKIE['lang'];
    }
    if (session_status() === PHP_SESSION_ACTIVE && isset($_SESSION['lang']) && in_array($_SESSION['lang'], $langs, true)) {
        return $_SESSION['lang'];
    }
    return 'zh';
}

